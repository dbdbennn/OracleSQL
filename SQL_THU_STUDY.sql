-- MAKE SEQUENCE
DROP SEQUENCE SEQUENCE;
CREATE SEQUENCE SEQUENCE INCREMENT BY 10 START WITH 1;

-- SEQUENCE 적용
CREATE TRIGGER PRODUCTTBL_TRG 
BEFORE INSERT ON PRODUCTTBL 
FOR EACH ROW 
BEGIN
  <<COLUMN_SEQUENCES>>
  BEGIN
    IF INSERTING AND :NEW.IDNUM IS NULL THEN
      SELECT SEQUENCE.NEXTVAL INTO :NEW.IDNUM FROM SYS.DUAL;
    END IF;
  END COLUMN_SEQUENCES;
END;
/


